#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;

	if (!s || !(*s))
	{
		write(1, "\n", 1);
		return ;
	}
	write(1, s, ft_strlen(s));
	write(1, "\n", 1);
}
